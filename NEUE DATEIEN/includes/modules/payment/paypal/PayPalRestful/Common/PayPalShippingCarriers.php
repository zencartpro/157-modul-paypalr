    /**
     * Determine whether the given carrier code is valid with PayPal
     *
     * @param string $carrier_code
     * @return bool
     */
    public static function isValid(string $carrier_code): bool
    {
        foreach (explode(';', self::carriersCsv) as $line) {
            if (empty($line)) {
                continue;
            }
            list($name, $code, $country) = explode(',', trim($line));
            if ($code === 'OTHER') {
                continue;
            }
            if ($code === $carrier_code) {
                return true;
            }
        }
        return false;
    }

    public static function findBestMatch(string $carrier_name, string $country_iso_3 = ''): string
    {
        $carrier_name = \strtoupper($carrier_name);
        if ($carrier_name === 'OTHER') {
            return 'OTHER';
        }

        $bestMatch = '';
        $carriersForCountry = self::getCarriersForCountry($country_iso_3, true);
        $carriersForCountry = \array_change_key_case($carriersForCountry, \CASE_UPPER);

        // if we have an exact match on code, just return it
        if (\array_key_exists($carrier_name, $carriersForCountry)) {
            return $carrier_name;
        }

        // find longest-matching name
        foreach ($carriersForCountry as $code => $name) {
            $name = \strtoupper($name);
            if (str_contains($carrier_name, $name)) {
                if (strlen($name) > strlen($bestMatch)) {
                    $bestMatch = $name;
                }
            }
        }

        // return the code for the best-matching name
        if (!empty($bestMatch)) {
            foreach ($carriersForCountry as $code => $name) {
                if ($bestMatch === \strtoupper($name)) {
                    return $code;
                }
            }
        }

        return 'OTHER'; // This supports PHP 7.0; for PHP 8 prefer to return null and change return type to ?string
    }

    /**
     * Lookup available carriers by country code.
     *
     * @param string $country_iso_3 Filter by country's 3-char ISO code, or 'GLOBAL'
     * @param bool $withGlobal Include global carriers as well? Globals appear after country-specific
     * @return array
     */
    public static function getCarriersForCountry(string $country_iso_3, bool $withGlobal = false): array
    {
        $found = [];
        $globals = [];
        foreach (explode(';', self::carriersCsv) as $line) {
            if (empty($line)) {
                continue;
            }
            list($name, $code, $country) = explode(',', trim($line));
            if ($code === 'OTHER') {
                continue;
            }
            if ($country === $country_iso_3) {
                $found[$code] = $name;
            }
            $globals[$code] = $name;
        }
        if (empty($found)) {
            return $globals;
        }
        if ($withGlobal === false) {
            return $found;
        }
        // put globals after local carriers
        return array_merge($found, $globals);
    }

